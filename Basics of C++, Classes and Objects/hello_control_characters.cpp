#include <iostream>
using namespace std;

int main() {
    // Hello World
    cout << "Hello World" << endl;

    // New line using \n
    cout << "This is first line\nThis is second line" << endl;

    // Tab using \t
    cout << "Name\tAge\tBranch" << endl;
    cout << "Aman\t20\tCSE" << endl;

    // Alarm sound using \a
    cout << "\a";   // May produce a beep sound

    // Carriage return using \r
    cout << "Hello World";
    cout << "\rHi";   // Overwrites starting characters

    return 0;
}
